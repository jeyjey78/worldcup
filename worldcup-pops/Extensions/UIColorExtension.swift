import UIKit

extension UIColor {
    @nonobjc class var messengerBlueLight: UIColor {
        return UIColor(red: 50.0 / 255.0, green: 142.0 / 255.0, blue: 247.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var messengerBlueDark: UIColor {
        return UIColor(red: 54.0 / 255.0, green: 82.0 / 255.0, blue: 223.0 / 255.0, alpha: 1.0)
    }
}
